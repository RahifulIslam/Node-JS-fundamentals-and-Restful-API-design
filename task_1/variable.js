// 4 types of variable in Javascript.

// 1.automatically
a = 2;
b = 3;
c = a+b;
console.log("addition of using autometically variable:", c);

// 2.var
var d = 4;
var e = 5;
var f = d+e;
console.log("addition of using var variable:", f);

// 3. let
let g = 4;
let h = 6;
let i = g+h;
console.log("Addition of using let variable:", i)

