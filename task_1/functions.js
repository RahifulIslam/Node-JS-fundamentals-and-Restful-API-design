// General function 
function square(number) {
    return number * number;
  }
  
  // Usage:
  console.log(square(5)); // Output: 25

  // Arrow function 
const cube = (number) => {
    return number * number * number;
  };
  
  // Usage:
  console.log(cube(3)); // Output: 27

